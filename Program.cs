app.Use(async (ctx, next) =>
{
    var path = ctx.Request.Path.Value ?? "/";
    var skipTelemetry =
        path.StartsWith("/css", StringComparison.OrdinalIgnoreCase) ||
        path.StartsWith("/js", StringComparison.OrdinalIgnoreCase) ||
        path.StartsWith("/lib", StringComparison.OrdinalIgnoreCase) ||
        path.StartsWith("/uploads", StringComparison.OrdinalIgnoreCase) ||
        path.StartsWith("/favicon", StringComparison.OrdinalIgnoreCase);

    if (skipTelemetry)
    {
        await next();
        return;
    }

    var sw = System.Diagnostics.Stopwatch.StartNew();
    string? error = null;
    try
    {
        await next();
    }
    catch (Exception ex)
    {
        error = ex.GetType().Name;
        throw;
    }
    finally
    {
        sw.Stop();
        var duration = sw.ElapsedMilliseconds;
        var status = ctx.Response.StatusCode;
        if (duration >= 1200 || status >= 500 || !string.IsNullOrWhiteSpace(error))
        {
            try
            {
                using var scope = app.Services.CreateScope();
                var db = scope.ServiceProvider.GetRequiredService<AppDbContext>();
                var email = scope.ServiceProvider.GetRequiredService<IAlertEmailService>();
                db.AppTelemetryEvents.Add(new AppTelemetryEvent
                {
                    CreatedAt = DateTime.UtcNow,
                    Path = path,
                    Method = ctx.Request.Method,
                    StatusCode = status,
                    DurationMs = duration,
                    IpAddress = ClientIpResolver.Get(ctx),
                    Username = ctx.User.Identity?.IsAuthenticated == true ? ctx.User.Identity?.Name : null,
                    Error = error
                });
                await db.SaveChangesAsync();

                var mustAlert = status >= 500 || duration >= 3000 || !string.IsNullOrWhiteSpace(error);
                if (mustAlert)
                {
                    var canSend = false;
                    lock (alertSync)
                    {
                        if ((DateTime.UtcNow - lastAlertUtc).TotalMinutes >= 5)
                        {
                            canSend = true;
                            lastAlertUtc = DateTime.UtcNow;
                        }
                    }

                    if (canSend)
                    {
                        var subject = $"[HN Nexus POS] Alerta {(status >= 500 ? "ERROR" : "LATENCIA")} {status} {path}";
                        var body = $"""
Fecha UTC: {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss}
Ruta: {ctx.Request.Method} {path}
Status: {status}
Duración: {duration} ms
Usuario: {ctx.User.Identity?.Name ?? "-"}
IP: {ClientIpResolver.Get(ctx)}
Error: {error ?? "-"}
""";
                        _ = await email.SendAlertAsync(subject, body);
                    }
                }
            }
            catch
            {
                // No romper request por fallo de telemetría.
            }
        }
    }
});

