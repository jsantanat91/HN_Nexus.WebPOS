{
    var db = scope.ServiceProvider.GetRequiredService<AppDbContext>();
    await db.Database.EnsureCreatedAsync();

    await db.Database.ExecuteSqlRawAsync(
        """
        CREATE TABLE IF NOT EXISTS "Suppliers" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "Name" text NOT NULL,
            "ContactName" text NOT NULL DEFAULT '',
            "Phone" text NOT NULL DEFAULT '',
            "Email" text NOT NULL DEFAULT '',
            "IsActive" boolean NOT NULL DEFAULT true
        );

        CREATE TABLE IF NOT EXISTS "Branches" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "Code" text NOT NULL,
            "Name" text NOT NULL,
            "Address" text NOT NULL,
            "IsActive" boolean NOT NULL DEFAULT true
        );

        CREATE TABLE IF NOT EXISTS "UserBranchAccesses" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "UserId" integer NOT NULL,
            "BranchId" integer NOT NULL
        );

        CREATE TABLE IF NOT EXISTS "PermissionProfiles" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "Name" text NOT NULL,
            "ModulePermissions" text NOT NULL,
            "IsAdmin" boolean NOT NULL DEFAULT false,
            "IsActive" boolean NOT NULL DEFAULT true
        );

        CREATE TABLE IF NOT EXISTS "AuditLogs" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "CreatedAt" timestamp with time zone NOT NULL,
            "Action" text NOT NULL,
            "Entity" text NOT NULL,
            "EntityId" integer NULL,
            "BranchId" integer NULL,
            "Username" text NOT NULL DEFAULT '',
            "IpAddress" text NOT NULL DEFAULT '',
            "Details" text NOT NULL DEFAULT ''
        );

        CREATE TABLE IF NOT EXISTS "ProductStocks" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "ProductId" integer NOT NULL,
            "BranchId" integer NOT NULL,
            "Stock" integer NOT NULL DEFAULT 0,
            "MinStock" integer NOT NULL DEFAULT 5
        );

        CREATE TABLE IF NOT EXISTS "StockTransfers" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "ProductId" integer NOT NULL,
            "FromBranchId" integer NOT NULL,
            "ToBranchId" integer NOT NULL,
            "Quantity" integer NOT NULL DEFAULT 0,
            "Notes" text NOT NULL DEFAULT '',
            "UserId" integer NULL,
            "CreatedAt" timestamp with time zone NOT NULL
        );

        CREATE TABLE IF NOT EXISTS "CashShifts" (
            "Id" integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            "BranchId" integer NOT NULL,
            "UserId" integer NOT NULL,
            "OpenedAt" timestamp with time zone NOT NULL,
            "OpeningFloat" numeric(18,2) NOT NULL DEFAULT 0,
            "ClosedAt" timestamp with time zone NULL,
            "ClosingDeclared" numeric(18,2) NULL,
            "SystemCashTotal" numeric(18,2) NULL,
            "Difference" numeric(18,2) NULL,
            "Status" text NOT NULL DEFAULT 'Open'
        );
        ALTER TABLE "CashShifts" ADD COLUMN IF NOT EXISTS "ClosingSignature" text NOT NULL DEFAULT '';
        ALTER TABLE "CashShifts" ADD COLUMN IF NOT EXISTS "ClosingNotes" text NOT NULL DEFAULT '';
        ALTER TABLE "CashShifts" ADD COLUMN IF NOT EXISTS "ClosingEvidencePath" text NULL;

        ALTER TABLE "Products" ADD COLUMN IF NOT EXISTS "ProductNumber" integer NOT NULL DEFAULT 0;
        ALTER TABLE "Products" ADD COLUMN IF NOT EXISTS "PriceIncludesTax" boolean NOT NULL DEFAULT false;
        ALTER TABLE "Products" ADD COLUMN IF NOT EXISTS "PromotionType" text NOT NULL DEFAULT 'None';
        ALTER TABLE "Products" ADD COLUMN IF NOT EXISTS "PromotionValue" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "Products" ADD COLUMN IF NOT EXISTS "PromotionMinQty" integer NOT NULL DEFAULT 0;

        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "BranchId" integer NOT NULL DEFAULT 1;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "SubtotalAmount" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "TaxAmount" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "DiscountAmount" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "PaymentMethod" text NOT NULL DEFAULT 'Cash';
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "AuthorizationCode" text NULL;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "PricesIncludeTax" boolean NOT NULL DEFAULT false;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "AmountReceived" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "ChangeAmount" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "CancelledAt" timestamp with time zone NULL;
        ALTER TABLE "Sales" ADD COLUMN IF NOT EXISTS "CancelReason" text NULL;

        ALTER TABLE "SaleDetails" ADD COLUMN IF NOT EXISTS "DiscountPercent" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "SaleDetails" ADD COLUMN IF NOT EXISTS "DiscountAmount" numeric(18,2) NOT NULL DEFAULT 0;

        ALTER TABLE "Users" ADD COLUMN IF NOT EXISTS "ModulePermissions" text NOT NULL DEFAULT 'dashboard,sales,products,customers,expenses,cashcuts,supplierorders,reports,cashshifts';
        ALTER TABLE "Users" ADD COLUMN IF NOT EXISTS "PermissionProfileId" integer NULL;

        ALTER TABLE "AppConfigs" ADD COLUMN IF NOT EXISTS "CurrencySymbol" text NOT NULL DEFAULT '$';
        ALTER TABLE "AppConfigs" ADD COLUMN IF NOT EXISTS "TaxRate" numeric(18,2) NOT NULL DEFAULT 16;
        ALTER TABLE "AppConfigs" ADD COLUMN IF NOT EXISTS "TicketPrinterName" text NULL;

        ALTER TABLE "CashCuts" ADD COLUMN IF NOT EXISTS "BranchId" integer NOT NULL DEFAULT 1;
        ALTER TABLE "CashCuts" ADD COLUMN IF NOT EXISTS "CashSales" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "CashCuts" ADD COLUMN IF NOT EXISTS "CardSales" numeric(18,2) NOT NULL DEFAULT 0;
        ALTER TABLE "CashCuts" ADD COLUMN IF NOT EXISTS "TransferSales" numeric(18,2) NOT NULL DEFAULT 0;

        ALTER TABLE "SupplierOrders" ADD COLUMN IF NOT EXISTS "SupplierId" integer NOT NULL DEFAULT 1;
        ALTER TABLE "SupplierOrders" ADD COLUMN IF NOT EXISTS "ProductId" integer NOT NULL DEFAULT 1;
        ALTER TABLE "SupplierOrders" ADD COLUMN IF NOT EXISTS "BranchId" integer NOT NULL DEFAULT 1;
        ALTER TABLE "SupplierOrders" ADD COLUMN IF NOT EXISTS "UnitCost" numeric(18,2) NOT NULL DEFAULT 0;

        ALTER TABLE "Customers" ADD COLUMN IF NOT EXISTS "CfdiUse" text NOT NULL DEFAULT 'G03';
        ALTER TABLE "Customers" ADD COLUMN IF NOT EXISTS "FiscalRegime" text NOT NULL DEFAULT '601';
        ALTER TABLE "Customers" ADD COLUMN IF NOT EXISTS "InvoiceType" text NOT NULL DEFAULT 'I';
        ALTER TABLE "Customers" ADD COLUMN IF NOT EXISTS "PaymentForm" text NOT NULL DEFAULT '01';
        ALTER TABLE "Customers" ADD COLUMN IF NOT EXISTS "PaymentMethodSat" text NOT NULL DEFAULT 'PUE';

        CREATE UNIQUE INDEX IF NOT EXISTS "IX_UserBranchAccesses_User_Branch" ON "UserBranchAccesses" ("UserId", "BranchId");
        CREATE UNIQUE INDEX IF NOT EXISTS "IX_ProductStocks_Product_Branch" ON "ProductStocks" ("ProductId", "BranchId");

        INSERT INTO "Branches" ("Code", "Name", "Address", "IsActive")
