app.Use(async (ctx, next) =>
{
    static string SanitizeSchema(string? raw)
    {
        if (string.IsNullOrWhiteSpace(raw))
        {
            return "public";
        }

        var clean = raw.Trim().ToLowerInvariant();
        return System.Text.RegularExpressions.Regex.IsMatch(clean, "^[a-z_][a-z0-9_]{0,62}$")
            ? clean
            : "public";
    }

    var schema = "public";
    if (ctx.User.Identity?.IsAuthenticated == true)
    {
        schema = SanitizeSchema(ctx.User.FindFirst("tenant_schema")?.Value);
    }
    else
    {
        var host = (ctx.Request.Host.Host ?? string.Empty).ToLowerInvariant();
        if (!string.IsNullOrWhiteSpace(host))
        {
            try
            {
                using var scope = app.Services.CreateScope();
                var db = scope.ServiceProvider.GetRequiredService<AppDbContext>();
                var fromHost = await db.Tenants
                    .Where(t => t.IsActive && t.Host != null && t.Host.ToLower() == host)
                    .Select(t => t.SchemaName)
                    .FirstOrDefaultAsync();
                schema = SanitizeSchema(fromHost);
            }
            catch
            {
                schema = "public";
            }
        }
    }

    try
    {
        var db = ctx.RequestServices.GetRequiredService<AppDbContext>();
        await db.Database.ExecuteSqlRawAsync($"SET search_path TO \"{schema}\", public;");
    }
    catch
    {
        // Si no se puede fijar tenant, mantener flujo para no bloquear login o estáticos.
    }

    await next();
});

