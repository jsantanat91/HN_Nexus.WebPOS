        SELECT 'Proveedor General', '', '', '', true
        WHERE NOT EXISTS (SELECT 1 FROM "Suppliers");

        INSERT INTO "PermissionProfiles" ("Name", "ModulePermissions", "IsAdmin", "IsActive")
        SELECT 'Administrador', 'dashboard,sales,products,customers,expenses,cashcuts,cashshifts,supplierorders,reports,config,users,branches', true, true
        WHERE NOT EXISTS (SELECT 1 FROM "PermissionProfiles" WHERE "Name" = 'Administrador');

        INSERT INTO "PermissionProfiles" ("Name", "ModulePermissions", "IsAdmin", "IsActive")
        SELECT 'Caja', 'dashboard,sales,customers,cashcuts,cashshifts', false, true
        WHERE NOT EXISTS (SELECT 1 FROM "PermissionProfiles" WHERE "Name" = 'Caja');
        """);

    // Normaliza consecutivo para evitar duplicados previos (incluyendo ceros) y luego crea índice único.
    await db.Database.ExecuteSqlRawAsync(
        """
        WITH renumbered AS (
            SELECT
                "Id",
                ROW_NUMBER() OVER (
                    ORDER BY
                        CASE WHEN "ProductNumber" IS NULL OR "ProductNumber" = 0 THEN 1 ELSE 0 END,
                        "ProductNumber",
                        "Id"
                ) AS rn
            FROM "Products"
        )
        UPDATE "Products" p
        SET "ProductNumber" = r.rn
        FROM renumbered r
        WHERE p."Id" = r."Id";

        CREATE UNIQUE INDEX IF NOT EXISTS "IX_Products_ProductNumber" ON "Products" ("ProductNumber");
        """);

    if (!await db.Users.AnyAsync(u => u.Username == "admin"))
    {
        db.Users.Add(new User
        {
            Username = "admin",
            PasswordHash = BCrypt.Net.BCrypt.HashPassword("admin123"),
            FullName = "Administrador",
            Role = "Admin",
            ModulePermissions = string.Join(',', ModuleCatalog.All),
            IsActive = true
        });
        await db.SaveChangesAsync();
    }

    var admin = await db.Users.FirstOrDefaultAsync(u => u.Username == "admin");
    var branch = await db.Branches.OrderBy(b => b.Id).FirstOrDefaultAsync();

    if (admin is not null)
    {
        admin.ModulePermissions = string.Join(',', ModuleCatalog.All);
        admin.PermissionProfileId ??= await db.PermissionProfiles
            .Where(p => p.IsAdmin)
            .OrderBy(p => p.Id)
            .Select(p => (int?)p.Id)
            .FirstOrDefaultAsync();

        if (branch is not null)
        {
            var hasAccess = await db.UserBranchAccesses.AnyAsync(x => x.UserId == admin.Id && x.BranchId == branch.Id);
            if (!hasAccess)
            {
                db.UserBranchAccesses.Add(new UserBranchAccess { UserId = admin.Id, BranchId = branch.Id });
            }
        }

        await db.SaveChangesAsync();
    }
}

if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Error");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseStaticFiles();
app.UseRouting();
app.UseAuthentication();
app.UseAuthorization();

app.Use(async (ctx, next) =>
{
    if (ctx.User.Identity?.IsAuthenticated == true && !ctx.User.IsInRole("Admin"))
    {
        var path = ctx.Request.Path.Value ?? "/";
        var route = moduleRoutes
            .OrderByDescending(x => x.Key.Length)
            .FirstOrDefault(x => path.StartsWith(x.Key, StringComparison.OrdinalIgnoreCase));

        if (!string.IsNullOrWhiteSpace(route.Key))
        {
            var modules = (ctx.User.FindFirstValue("modules") ?? string.Empty)
                .Split(',', StringSplitOptions.RemoveEmptyEntries | StringSplitOptions.TrimEntries)
                .Select(x => x.ToLowerInvariant())
                .ToHashSet();

            if (!modules.Contains(route.Value))
            {
                ctx.Response.Redirect("/Account/Denied");
                return;
            }
        }
    }

    await next();
});

app.MapRazorPages();

app.Run();
