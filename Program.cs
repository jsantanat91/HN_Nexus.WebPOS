            ModulePermissions = string.Join(',', ModuleCatalog.All),
            IsActive = true
        });
        await db.SaveChangesAsync();
    }

    var admin = await db.Users.FirstOrDefaultAsync(u => u.Username == "admin");
    var branch = await db.Branches.OrderBy(b => b.Id).FirstOrDefaultAsync();

    if (admin is not null)
    {
        admin.Role = "SuperUser";
        admin.IsSuperUser = true;
        admin.TenantId ??= await db.Tenants
            .Where(t => t.SchemaName == "public")
            .Select(t => (int?)t.Id)
            .FirstOrDefaultAsync();
        admin.ModulePermissions = string.Join(',', ModuleCatalog.All);
        admin.PermissionProfileId ??= await db.PermissionProfiles
            .Where(p => p.IsAdmin)
            .OrderBy(p => p.Id)
            .Select(p => (int?)p.Id)
            .FirstOrDefaultAsync();

        if (branch is not null)
        {
            var hasAccess = await db.UserBranchAccesses.AnyAsync(x => x.UserId == admin.Id && x.BranchId == branch.Id);
            if (!hasAccess)
            {
                db.UserBranchAccesses.Add(new UserBranchAccess { UserId = admin.Id, BranchId = branch.Id });
            }
        }

        await db.SaveChangesAsync();
    }

    var principalTenantIdForUsers = await db.Tenants
        .Where(t => t.SchemaName == "public")
        .Select(t => (int?)t.Id)
        .FirstOrDefaultAsync();
    if (principalTenantIdForUsers.HasValue)
    {
        var usersWithoutTenant = await db.Users
            .Where(u => !u.IsSuperUser && u.TenantId == null)
            .ToListAsync();
        foreach (var u in usersWithoutTenant)
        {
            u.TenantId = principalTenantIdForUsers.Value;
        }
        if (usersWithoutTenant.Count > 0)
        {
            await db.SaveChangesAsync();
        }
    }
}

